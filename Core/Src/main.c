void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
	{
		if(htim == &htim2)
		{
		event.upTimeMilli = HAL_GetTick();
		uint32_t sysValue = SysTick->VAL;
		
		if(event.upTimeMilli != HAL_GetTick())
			{
				event.upTimeMilli = HAL_GetTick();
				sysValue = SysTick->VAL;
			}
			event.upTimeMicro = event.upTimeMilli * 1000 - sysValue/((SysTick->LOAD + 1)/1000);
			event.currHallEvent = event.upTimeMicro;
			event.hallStepPeriodMicro = event.currHallEvent - event.prevHallEvent;
			event.prevHallEvent = event.currHallEvent;
		}
		 
	}
	

